    private void btnInicioSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInicioSesionActionPerformed

        String usuario = txtIngresoNombreUsuario.getText();
        String contrasena = jPswIngresoUsuario.getText();
        boolean resp = new DaoUsuario().validarContrasena(usuario, contrasena);
        if (resp == true) {
            JOptionPane.showMessageDialog(null, "Usuario/contrase침a validados");
            JfrmVistaHome main=new JfrmVistaHome();
            main.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Error usuario o contrase침a incorrectos. En caso de requerir una cuenta pong치se en contacto con un administrador.");
        }
        
        
    }//GEN-LAST:event_btnInicioSesionActionPerformed

